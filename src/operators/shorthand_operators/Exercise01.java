package operators.shorthand_operators;

public class Exercise01 {
    public static void main(String[] args) {

        int a = 8, b = 3;

        a += b;
        System.out.println(a);
        System.out.println(b);
        a -= b;
        System.out.println(a);
        System.out.println(b);
        a *= b;
        System.out.println(a);
        System.out.println(b);
        a /= b;
        System.out.println(a);
        System.out.println(b);

        a %= b;
        System.out.println(a);
        System.out.println(b);


    }


}
