package loops;

public class Exercise01_DescendingNumbers {
    public static void main(String[] args) {
        //Write a java program to print numbers starting from 100 to 0 (100 and 0 are included)

        //start point: 100
        //end point: 0
        //update --
        for (int i = 100; i >= 0 ; i--) {
            System.out.println(i);
        }
    }
}
