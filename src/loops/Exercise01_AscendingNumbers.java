package loops;

public class Exercise01_AscendingNumbers {
    public static void main(String[] args) {
        //Write a java program that prints numbers from 1 to 10 are included
        // start point: 1
        // end point: 10
        // update: increment

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

    }
}
